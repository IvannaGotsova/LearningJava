
public class Person {

	String firstName;
	String middleName;
	String lastName;
	String city;
	int age;
	
	Person(String firstName, String middleName, String lastName, String city, int age) {
		this.firstName = firstName;
		this.middleName = middleName;
		this.lastName = lastName;
		this.city = city;
		this.age = age;
	}
	
}
